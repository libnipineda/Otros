    public Ventana() {
        c = new ComSer();

        initComponents();
        hil = new Thread(new Runnable() {
            @Override
            public void run() {
                int est = 0;
                String cad = "";
                int con = 0;
                while (true) {
                    try {
                        int car = c.lee();

                        if (car != -1) {
                            switch (est) {
                                case 0:
                                    cad += "X: " + (char) car + " - ";
                                    est = 1;
                                    break;
                                case 1:
                                    cad += "Y: " + (char) car;
                                    est = 2;
                                    break;
                                case 2:
                                    cad += (char) car + " - ";
                                    est = 3;
                                    break;
                                case 3:
//                                    if ((char) car == '>') {
//                                        cad += " DES ";
//                                    }else{
//                                        cad += " ACT ";
//                                    }
                                    cad += (char) car;
                                    
                                    jTextField2.setText("");
                                    jTextField2.setText(cad + " act: " + con);
                                    con++;
                                    est = 0;
                                    cad = "";
                                    break;
                            }

                            
                        }

                    } catch (Exception e) {
                    }
                }
            }
        });
    }

